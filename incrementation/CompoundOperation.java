package incrementation;

public class CompoundOperation {

	public static void main(String[] args) {
       int i=10;
       
       System.out.println("Before compound operation i= "+i);
       
       i-=2;
       System.out.println("Afer compound operation i ="+i);
       
       i*=3;
       System.out.println("Ater compound Operation i ="+i);
       
       i-=3;
       System.out.println("Ater compound Operation i ="+i);
       
       
       
	}

}
